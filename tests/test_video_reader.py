"""Unit tests for the decord-compatible VideoReader shim (PyAV+TOC backend).

Generates a small synthetic clip per test via ffmpeg's ``testsrc`` filter
and exercises every shim entry point that downstream call sites depend
on. The parity-harness regression gate
(``tests/decord_pyav_parity/run_parity.py``) covers byte-exact frame
equivalence against the ffmpeg-CLI oracle; this module covers API
shape only.
"""
from __future__ import annotations

import shutil
import subprocess
from pathlib import Path

import numpy as np
import pytest


FPS = 24
DURATION_S = 1
N_FRAMES = FPS * DURATION_S  # 24


def _have_ffmpeg() -> bool:
    return shutil.which("ffmpeg") is not None


pytestmark = pytest.mark.skipif(not _have_ffmpeg(), reason="ffmpeg not on PATH")


@pytest.fixture(scope="module")
def synthetic_clip(tmp_path_factory) -> Path:
    """A tiny 24fps, 1s, 64x48 H.264 clip generated by ffmpeg's testsrc."""
    out = tmp_path_factory.mktemp("video_reader") / "testsrc.mp4"
    cmd = [
        "ffmpeg", "-y", "-hide_banner", "-loglevel", "error",
        "-f", "lavfi", "-i", f"testsrc=duration={DURATION_S}:size=64x48:rate={FPS}",
        "-c:v", "libx264", "-pix_fmt", "yuv420p",
        "-g", "12",
        str(out),
    ]
    subprocess.run(cmd, check=True)
    assert out.exists() and out.stat().st_size > 0
    return out


