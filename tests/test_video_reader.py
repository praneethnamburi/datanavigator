def test_video_gray_uses_rgb_to_gray(synthetic_clip):
    """Regression gate for the 1.3.0 RGB/BGR fix.

    Confirms gray() routes RGB→GRAY (not BGR→GRAY). Verified by
    constructing a synthetic red frame whose luma under BGR→GRAY
    would equal the blue-channel coefficient and under RGB→GRAY
    would equal the red-channel coefficient. We assert against the
    RGB→GRAY expectation.
    """
    from datanavigator import Video
    import cv2 as cv

    v = Video(str(synthetic_clip))
    gray = v.gray(0)
    assert isinstance(gray, np.ndarray)
    assert gray.ndim == 2
    assert gray.dtype == np.uint8
    assert gray.shape == (48, 64)

    # Round-trip: applying the same conversion to the asnumpy()-fetched
    # frame should reproduce gray() exactly. This pins the channel order
    # used inside gray(); any silent revert to COLOR_BGR2GRAY would
    # change the right-hand side and break equality.
    expected = cv.cvtColor(v[0].asnumpy(), cv.COLOR_RGB2GRAY)
    assert np.array_equal(gray, expected)


