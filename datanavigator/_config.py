CLIP_FOLDER = 'C:\\data\\_clipcollection'
CACHE_FOLDER = 'C:\\data\\_cache'

def set_clip_folder(folder):
    global CLIP_FOLDER
    CLIP_FOLDER = folder

def get_clip_folder():
    return CLIP_FOLDER

def set_cache_folder(folder):
    global CACHE_FOLDER
    CACHE_FOLDER = folder

def get_cache_folder():
    return CACHE_FOLDER
